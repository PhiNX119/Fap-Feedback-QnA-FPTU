    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //Lay ra ma so sinh vien
        HttpSession session = request.getSession();
        if (session.getAttribute("username") == null) {
            request.getRequestDispatcher("login").forward(request, response);
        } else {
            Student loginStudentData = (Student) session.getAttribute("loginStudentData");
            //Lay ra danh sach mon hoc cua sinh vien tren
            Subject subject = new Subject();
            ArrayList<Subject> listSubject = subject.getListSubject(loginStudentData.getStudentId());

            session.setAttribute("listSubject", listSubject);
            request.getRequestDispatcher("ListFeedback.jsp").forward(request, response);
        }
    }

